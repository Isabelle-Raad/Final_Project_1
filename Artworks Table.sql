CREATE TABLE Artists (
  ConstituentID VARCHAR(10) NOT NULL,
  DisplayName VARCHAR(40) NOT NULL,
  ArtistBio VARCHAR(40) NOT NULL,
  Nationality VARCHAR(40) NOT NULL,
  Gender VARCHAR(10) NOT NULL,
  BeginDate VARCHAR NOT NULL,
  EndDate VARCHAR NOT NULL,
  Wiki_QID VARCHAR(20),	
  ULAN VARCHAR(20),
  PRIMARY KEY (ConstituentID)
);

CREATE TABLE Artworks (
	Title VARCHAR (40) NOT NULL,
	Artist VARCHAR(40) NOT NULL,
	ConstituentID VARCHAR (20) NOT NULL,
	ArtistBio VARCHAR (40) NOT NULL,
	Nationality VARCHAR (20) NOT NULL,
    BeginDate DATE NOT NULL,
    EndDate VARCHAR(20) NOT NULL,
	Gender VARCHAR (20) NOT NULL,
	Date DATE NOT NULL,
	Medium VARCHAR (40) NOT NULL,
	Dimensions INT NOT NULL,
	CreditLine VARCHAR (40) NOT NULL,
	AccessionNumber INT NOT NULL,
	Classification VARCHAR (20) NOT NULL,
	Department VARCHAR (30) NOT NULL,
	DateAcquired DATE NOT NULL,
	Cataloged VARCHAR (30) NOT NULL,
	ObjectID INT NOT NULL,
	URL VARCHAR (60) NOT NULL,
	ThumbnailURL VARCHAR (60) NOT NULL,
	Circumference INT NOT NULL,
	Depth INT NOT NULL,
	Diameter INT NOT NULL,
	Height INT NOT NULL,
	Length INT NOT NULL,
	Weight INT NOT NULL,
	Width INT NOT NULL,
	Seat_Height INT NOT NULL,
	Duration INT NOT NULL,	
	PRIMARY KEY (Title)
);
